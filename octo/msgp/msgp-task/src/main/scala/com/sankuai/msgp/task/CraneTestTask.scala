package com.sankuai.msgp.task

import java.text.SimpleDateFormat
import java.util.Date

import com.sankuai.msgp.common.utils.helper.CommonHelper
import org.slf4j.LoggerFactory

/**
  * Created by yves on 17/1/10.
  */
object CraneTestTask {
  private val logger = LoggerFactory.getLogger(CraneTestTask.getClass)
  private val DATE_TIME_FORMAT: SimpleDateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss")

  def init() = {
    val isOffline = CommonHelper.isOffline
    val current: String = DATE_TIME_FORMAT.format(new Date(System.currentTimeMillis / 1000L / 60L * 60L * 1000L))
    val logMessage: String = "Log auto-generated by task service. time: " + current + ", isOffline: " + isOffline
    logger.info(logMessage)
  }
}
