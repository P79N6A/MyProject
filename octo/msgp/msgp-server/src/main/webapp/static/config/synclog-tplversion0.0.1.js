M.add('msgp-config/synclog-tplversion0.0.1', function (Y, NAME) {
    Y.namespace('msgp.config.panel.synclog').template = Y.Template.Micro.compile([
        '<div id="<%= data.css_prefix %>-anchor" class="<%= data.css_prefix %> J-<%= data.css_prefix %>">'
        , '    <% if (data.version) { %>'
        , '    最新版本: <span class="config-node-synclog-version"><%= data.version %></span>'
        , '    <% } %>'
        , '    <span>(只展示最新的50条操作人操作日志)</span>'
        , '    <table class="table table-bordered">'
        , '    <% if (data.logs.length === 0) { %>'
        , '        <tr><td>还没有记录</td></tr>'
        , '    <% } else { %>'
        , '        <colgroup>'
        , '            <col width="10%"></col>'
        , '            <col width="8%"></col>'
        , '            <col width="5%"></col>'
        , '            <col width="30%"></col>'
        , '        </colgroup>'
        , '        <thead>'
        , '        <tr>'
        , '        <% Y.Array.each(data.keys, function(key) { %>'
        , '            <th><%= key %></th>'
        , '        <% }) %>'
        , '        </tr>'
        , '        </thead>'
        , '        <% Y.Array.forEach(data.logs, function(log, i) { %>'
        , '            <tr>'
        , '            <% Y.Array.each(log, function(value) { %>'
        , '            <td style="word-break: break-all;"><%== value.replace("修改后","<br>修改后") %></td>'
        , '            <% }) %>'
        , '            </tr>'
        , '        <% }) %>'
        , '    <% } %>'
        , '    </table>'
        , '</div>'
    ].join('\n'));
}, '0.1.0', { requires: [ 'template', 'template-micro' ] });